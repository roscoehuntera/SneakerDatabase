DROP TABLE IF EXISTS SHIPPING_T;
DROP TABLE IF EXISTS RELEASED_T;
DROP TABLE IF EXISTS PACKAGE_T;
DROP TABLE IF EXISTS PRODUCT_T;
DROP TABLE IF EXISTS SALARY_T;
DROP TABLE IF EXISTS HOURLY_T;
DROP TABLE IF EXISTS EMPLOYEE_T;
DROP TABLE IF EXISTS CUSTOMER_T;
DROP TABLE IF EXISTS DISTRIBUTOR_T;
DROP TABLE IF EXISTS MANUFACTURER_T; 


CREATE TABLE MANUFACTURER_T (
    NAME		VARCHAR(15),
    ADDRESS		VARCHAR(40),
    PHONE_NUM	VARCHAR(11),
    PROFIT		INT,
    
    CONSTRAINT MANUFACTURER_PK PRIMARY KEY (NAME)
    );
    
CREATE TABLE DISTRIBUTOR_T (
    STORE_ID	INT,
    ADDRESS		VARCHAR(40),
    FIRST_PARTY	BOOLEAN,
    PHONE_NUM	VARCHAR(11),
    
    CONSTRAINT DISTRIBUTOR_PK PRIMARY KEY (STORE_ID)
    );
    
CREATE TABLE CUSTOMER_T (
    CUSTOMER_ID	INT,
    NAME		VARCHAR(40),
    ADDRESS		VARCHAR(40),
    
    CONSTRAINT CUSTOMER_PK PRIMARY KEY (CUSTOMER_ID)
    );
    
CREATE TABLE EMPLOYEE_T (
    EMPLOYEE_ID	INT,
    NAME		VARCHAR(40),
    DATE_HIRED	DATE,
    EMPLOYEE_TYPE	VARCHAR(40),
    STORE_ID	INT,
    
    CONSTRAINT EMPLOYEE_PK PRIMARY KEY (EMPLOYEE_ID),
    CONSTRAINT EMPLOYEE_FK FOREIGN KEY (STORE_ID) REFERENCES DISTRIBUTOR_T(STORE_ID)
    );
    
CREATE TABLE HOURLY_T (
    EMPLOYEE_ID	INT,
    RATE		INT,
    
    CONSTRAINT HOURLY_FK FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE_T(EMPLOYEE_ID)
    );
    
CREATE TABLE SALARY_T (
    EMPLOYEE_ID	INT,
    SALARY		INT,
    
    CONSTRAINT SALARY_FK FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE_T(EMPLOYEE_ID)
    );

CREATE TABLE PRODUCT_T (
    PRODUCT_ID	INT,
    COLORWAY	VARCHAR(25),
    DESIGNER	VARCHAR(40),
    MODEL		VARCHAR(25),
    NAME		VARCHAR(25),
    STORE_ID	INT,
    
    CONSTRAINT PRODUCT_PK PRIMARY KEY (PRODUCT_ID),
    CONSTRAINT PRODUCT_FK1 FOREIGN KEY (NAME) REFERENCES MANUFACTURER_T(NAME),
    CONSTRAINT PRODUCT_FK2 FOREIGN KEY (STORE_ID) REFERENCES DISTRIBUTOR_T(STORE_ID)
    );
    
CREATE TABLE PACKAGE_T (
    PACKAGE_NUM	INT,
    CUSTOMER_ID	INT,
    PRODUCT_ID	INT,
    PRICE		INT,
    PURCHASED	DATE,
    
    CONSTRAINT PACKAGE_PK PRIMARY KEY (PACKAGE_NUM),
    CONSTRAINT PACKAGE_FK1 FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER_T(CUSTOMER_ID),
    CONSTRAINT PACKAGE_FK2 FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT_T(PRODUCT_ID)
    );

CREATE TABLE RELEASED_T (
    PRODUCT_ID	INT,
    PACKAGE_NUM	INT,
    RELEASED	DATE,
    RESTOCK		BOOLEAN,
    TYPE		VARCHAR(40),
    
    CONSTRAINT RELEASED_FK1 FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT_T(PRODUCT_ID),
    CONSTRAINT RELEASED_FK2 FOREIGN KEY (PACKAGE_NUM) REFERENCES PACKAGE_T(PACKAGE_NUM)
    );
    
CREATE TABLE SHIPPING_T (
    SHIPPING_ID	INT,
    PROVIDER	VARCHAR(40),
    ARRIVAL_DATE	DATE,
    CUSTOMER_ID	INT,
    
    CONSTRAINT SHIPPING_PK PRIMARY KEY (SHIPPING_ID),
    CONSTRAINT SHIPPING_FK1 FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER_T(CUSTOMER_ID)
    );
    
CREATE TABLE DISTRIBUTOR_SHIPPING_T (
    STORE_ID	INT,
    SHIPPING_ID	INT,
    
    CONSTRAINT DISTRIBUTOR_SHIPPING_FK1 FOREIGN KEY (STORE_ID) REFERENCES DISTRIBUTOR_T(STORE_ID),
    CONSTRAINT DISTRIBUTOR_SHIPPING_FK2 FOREIGN KEY (SHIPPING_ID) REFERENCES SHIPPING_T(SHIPPING_ID)
    );
    
CREATE TABLE MANUFACTURER_DISTRIBUTOR_T (
    NAME		VARCHAR(25),
    STORE_ID	INT,
    
    CONSTRAINT MANUFACTURER_DISTRIBUTOR_FK1 FOREIGN KEY (NAME) REFERENCES MANUFACTURER_T(NAME),
    CONSTRAINT MANUFACTURER_DISTRIBUTOR_FK2 FOREIGN KEY (STORE_ID) REFERENCES DISTRIBUTOR_T(STORE_ID)
    ); 

